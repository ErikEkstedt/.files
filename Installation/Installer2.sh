#!/bin/bash
# Require sudo right
if [[ $UID != 0 ]]; then
    echo "Please run this script with sudo :"
    echo "sudo $0 $*"
    exit 1
fi

# Install (https://github.com/Airblader/i3/wiki/Compiling-&-Installing)
echo "########################################################"
echo "########################################################"
echo "########################################################"
echo "########################################################"
echo "########################################################"
echo
echo Download i3-gaps...
cd ~
git clone https://www.github.com/Airblader/i3 i3-gaps

echo "########################################################"
echo "########################################################"
echo "########################################################"
echo "########################################################"
echo "########################################################"
echo
echo
echo
echo Compile and install i3-gaps...
cd i3-gaps
autoreconf --force --install
rm -rf build/
mkdir -p build && cd build/
echo "########################################################"
echo "########################################################"
echo "###################     MAKE    #####################################"
echo "########################################################"
echo "########################################################"

../configure --prefix=/usr --sysconfdir=/etc --disable-sanitizers
make -j8
make install
echo "########################################################"
echo Finished installing i3-gaps.
echo "########################################################"
echo "########################################################"
echo
echo
echo
echo "########################################################"
echo "########################################################"
echo Create links for config files...
~/.files/Installation/Link_maker
echo "########################################################"
echo "########################################################"
echo "########################################################"
echo RESTART COMPUTER "(in 10 seconds)"
sleep 10
sudo shutdown -r 0


